package com.huawei.datatype;

public class PayBusCardInfo {
    private String AID;
    private String Name;
    private String id;

    public String getAID() {
        return this.AID;
    }

    public void setAID(String str) {
        this.AID = str;
    }

    public String getId() {
        return this.id;
    }

    public void setId(String str) {
        this.id = str;
    }

    public String getName() {
        return this.Name;
    }

    public void setName(String str) {
        this.Name = str;
    }
}
